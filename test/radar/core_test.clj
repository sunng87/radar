(ns radar.core-test
  (:use clojure.test
        radar.core))

(deftest all-servers-test
  (let [config {:groups {:s1 ["192.168.1.101:6379" "192.168.1.102:6379"]
                         :s2 ["192.168.1.103:6379" "192.168.1.104:6379"
                              "192.168.1.110:6379"]}}]
    (is (= #{"192.168.1.101:6379"
             "192.168.1.102:6379"
             "192.168.1.103:6379"
             "192.168.1.104:6379"
             "192.168.1.110:6379"}
           (all-servers config)))))
